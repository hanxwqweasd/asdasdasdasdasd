function themeKey(room: RoomTemplate): string {
  const id = room.id.toLowerCase();
  for (const key of Object.keys(thematic)) if (id.includes(key)) return key;
  return room.ambience in ambienceFallback ? room.ambience : 'corridor';
}

function pickDirection(byte: number): RoomObservation['direction'] {
  return (['left', 'right', 'center', 'above', 'behind'] as const)[byte % 5]!;
}

export function makeRoomObservation(room: RoomTemplate, action: ObservationAction, seed: string, attempt: number): RoomObservation {
  const pair = thematic[themeKey(room)] ?? ambienceFallback[themeKey(room)] ?? ambienceFallback.corridor!;
  const pool = pair[action].length ? pair[action] : ambienceFallback.corridor![action];
  const digest = crypto.createHash('sha256').update(`${seed}:${room.id}:${action}`).digest();
  const index = (digest[0]! + attempt) % pool.length;
  const direction = pickDirection(digest[1]! + attempt);
  const intensity = (['faint', 'clear', 'urgent'] as const)[(digest[2]! + attempt) % 3]!;
  const scoredChoices = room.choices.map((choice, choiceIndex) => ({ choiceIndex, score: Number(choice.effects.danger ?? 0) + Math.max(0, -Number(choice.effects.nerve ?? 0)) * 0.7 + Math.max(0, Number(choice.effects.noise ?? 0)) * 0.35 - Number(choice.effects.clues ?? 0) * 1.5 - Number(choice.effects.keys ?? 0) * 2 }));
  scoredChoices.sort((a,b)=>a.score-b.score||a.choiceIndex-b.choiceIndex);
  const recommendedChoiceIndex = attempt >= 1 && scoredChoices.length ? scoredChoices[0]!.choiceIndex : null;
  const clueAwarded = attempt === 0 && digest[4]! % 4 === 0;
  const directionCopy = { left: 'Источник слева.', right: 'Источник справа.', center: 'Источник прямо перед вами.', above: 'Источник выше уровня потолка.', behind: 'Источник за спиной.' }[direction];
  const intensityCopy = { faint: 'Сигнал слабый, но повторяется.', clear: 'Признак различим уверенно.', urgent: 'Признак меняется — решение нужно принять быстро.' }[intensity];
  return {
    key: `${themeKey(room)}:${action}:${index}`,
    text: pool[index]!,
    detail: `${directionCopy} ${intensityCopy}`,
    direction,
    intensity,
    recommendedChoiceIndex,
    clueAwarded
  };
}

export function makeSceneObservation(sceneKey: string, action: ObservationAction, seed: string, attempt: number): RoomObservation {
  const room: RoomTemplate = { id: sceneKey, title: 'Дом', description: '', ambience: sceneKey.includes('radio') ? 'radio' : 'corridor', accent: '#8a6b48', choices: [{label:'',outcome:'',effects:{}},{label:'',outcome:'',effects:{}},{label:'',outcome:'',effects:{}}] };
  return makeRoomObservation(room, action, seed, attempt);
}
